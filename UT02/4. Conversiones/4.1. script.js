//Realiza una suma
4 + 5       // resultado = 9

//Pone el número delante y la cadena de texto detrás
4 + "5"     // resultado = "45"

//Suma los números y pone el texto detrás
4 + 5 + "6" // resultado = "96"

// Convierte cadenas a números
parseInt("34")          // resultado = 34
parseInt("89.76")       // resultado = 89

//Convierte cadenas a números reales
parseFloat("34")        // resultado = 34
parseFloat("89.76")     // resultado = 89.76
4 + 5 + parseInt("6")   // resultado = 15

//Convierte números a cadenas
(" " + 3400)            // resultado = "3400"
(" " + 3400).length     // resultado = 4

