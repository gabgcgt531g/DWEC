//Comparación de valores numéricos
30  ==  30       //  true
30  ==  30.0     //  true
5  !=  8         //  true
9  >  13         //  false
7.29  <=  7.28   //  false

//Comparación de cadenas
"Marta"  ==  "Marta"    //  true
"Marta"  ==  "marta"    //  false
"Marta"  >  "marta"     //  false
"Mark"  <  "Marta"      //  true

//Comparación de números con su cadena
"123" == 123            // true

//Comparación de números con cadenas, convirtiendo antes la cadena en número
parseInt("123") == 123            // true